#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int n1;
	int n2;

	n1 = 0;
	n2 = 0;
	while (s1[n1] != '\0')
		n1++;
	while (s2[n2] != '\0')
	{
		s1[n1] = s2[n2];
		n1++;
		n2++;
	}
	s1[n1] = '\0';
	return (s1);
}
