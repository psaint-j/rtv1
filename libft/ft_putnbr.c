#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < -2147483647)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		n = n * -1;
		ft_putchar('-');
	}
	if (n <= 9)
	{
		n = n + 48;
		ft_putchar(n);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
