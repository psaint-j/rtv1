#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*p1;
	const char	*p2;

	p1 = dst;
	p2 = src;
	while (n--)
		*p1++ = *p2++;
	return (dst);
}
